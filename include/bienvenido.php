  <h3>Bienvenido al Observatorio Laboral de Coquimbo</h3>
  <p>
    El Observatorio Laboral de Coquimbo (OLC) se enmarca en un proyecto nacional de instalación de observatorios laborales en todas las regiones del país. En términos operativos el trabajo del OLM se realiza de acuerdo a los lineamientos del Observatorio Nacional, instancia que define los productos y las metodologías de trabajo, y al mismo tiempo apoya y monitorea la instalación de los observatorios en las regiones.
